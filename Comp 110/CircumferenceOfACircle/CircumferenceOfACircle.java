class CircumferenceOfACircle{

   public static void main (String args[]){
   
   //decaration
   double radius;
   double circumference;
   final double PI = 3.14;
   
   //initialization
   radius = 7;
   circumference = 0;
   
   //circumference
   circumference = 2 * radius * PI;
   
   System.out.print("when radius is 7, circumference of this circle is = " + circumference);
   
   }
   
}
   
   